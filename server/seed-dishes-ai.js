function makeDish(name, cuisine, category) {
  const d = CAT[category] || CAT['Горячее'];
  const out = pick(d.output);
  return {
    name, cuisine, category,
    description: `${cuisine} кухня. Выход: ${out} г.`,
    temperature: d.temp, shelf_life: d.shelf,
    output: out, cooking_time: pick(d.time),
    calories: pick(d.kbju.cal), proteins: pick(d.kbju.pro),
    fats: pick(d.kbju.fat), carbs: pick(d.kbju.carb),
    technology: pick(d.tech),
  };
}

async function main() {
  console.log('🧠 Seed 5000 dishes with AI categories v2...\n');

  const db = new Database(DB_PATH);
  db.exec(`CREATE TABLE IF NOT EXISTS dish_catalog (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT NOT NULL,
    cuisine TEXT DEFAULT '',
    category TEXT DEFAULT '',
    description TEXT DEFAULT '',
    temperature TEXT DEFAULT '',
    shelf_life TEXT DEFAULT '',
    output REAL DEFAULT 0,
    cooking_time INTEGER DEFAULT 0,
    calories REAL DEFAULT 0,
    proteins REAL DEFAULT 0,
    fats REAL DEFAULT 0,
    carbs REAL DEFAULT 0,
    technology TEXT DEFAULT '',
    created_at TEXT DEFAULT (datetime('now'))
  )`);
  db.exec('DELETE FROM dish_catalog');

  // Build base set with hardcoded categories
  const base = [];
  for (const [cuisine, names] of Object.entries(CUISINES)) {
    for (const name of names) {
      const cat = CATEGORIES[name] || 'Горячее';
      base.push(makeDish(name, cuisine, cat));
    }
  }
  console.log(`✅ Base dishes: ${base.length} (all with cuisine & category)`);

  // Generate variants
  const all = [...base];
  let attempts = 0;
  while (all.length < TARGET && attempts < 100000) {
    attempts++;
    const src = pick(base);
    const parts = [src.name];
    if (Math.random() > 0.5) parts.push(pick(SUFFIXES));
    if (Math.random() > 0.6) parts.unshift(pick(PREFIXES));
    if (Math.random() > 0.7) parts[0] = pick(STYLES) + ' ' + parts[0];

    const newName = parts.join(' ');
    if (all.some(d => d.name.toLowerCase() === newName.toLowerCase())) continue;

    all.push(makeDish(newName, src.cuisine, src.category));
    if (all.length % 1000 === 0) console.log(`  progress: ${all.length}/${TARGET}`);
  }

  console.log(`\n✅ Generated: ${all.length} dishes`);

  // Insert
  const insert = db.prepare(`INSERT INTO dish_catalog
    (name, cuisine, category, description, temperature, shelf_life, output, cooking_time, calories, proteins, fats, carbs, technology)
    VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?)`);

  const insertMany = db.transaction((items) => {
    for (const d of items) {
      insert.run(d.name, d.cuisine, d.category, d.description,
        d.temperature, d.shelf_life, d.output, d.cooking_time,
        d.calories, d.proteins, d.fats, d.carbs, d.technology);
    }
  });
  insertMany(all);

  const count = db.prepare('SELECT COUNT(*) as c FROM dish_catalog').get().c;
  const sample = db.prepare('SELECT name, cuisine, category FROM dish_catalog LIMIT 5').all();

  console.log(`\n🎉 ${count} dishes in DB`);
  console.log('Sample:');
  for (const s of sample) console.log(`  ${s.name} | ${s.cuisine} | ${s.category}`);

  const cats = db.prepare('SELECT category, COUNT(*) as c FROM dish_catalog GROUP BY category ORDER BY c DESC').all();
  console.log('\nCategories:');
  for (const c of cats) console.log(`  ${c.category}: ${c.c}`);

  db.close();
}

main().catch(e => { console.error('❌', e.message); process.exit(1); });
